
// Os números entram como String(texto)
let str1 = prompt("Digite um número:");
let str2 = prompt("Digite outro número:");

// Conversão dos números em valor Numérico
let num1 = Number(str1);
let num2 = Number(str2);

// Soma e resposta
let sumNumbers = num1 + num2;
alert(`A soma de ${num1} e ${num2} é: ${sumNumbers}`);